    RESULT_MSG      VARCHAR(50 ) ,
    NUM_OF_ROWS     VARCHAR(4  ) ,
    PAGE_NO         NUMBER(4),
    WK_CNT         NUMBER(4),
    TOT_CNT         NUMBER(4),
