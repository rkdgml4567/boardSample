(
    BAT_YM          VARCHAR(6  ) NOT NULL,
    WK_GBN					VARCHAR(2 ) NOT NULL,
    AREA_CODE       VARCHAR(5 )  NOT NULL,
    CONTRACT_YM    VARCHAR(6  ) NOT NULL,
